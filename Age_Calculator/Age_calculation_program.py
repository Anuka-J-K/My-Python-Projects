# Age calculation program

birth_year = int(input("Enter your year of birth: ")) 
future_year = int(input("Enter a future year: ")) 

age = future_year - birth_year

print("Your age in", future_year, "will be:", age)
# This program calculates the age of a person in a future year based on their birth year. It takes user input for the birth year and the future year, performs the calculation, and then displays the result.